package zw.co.elearning.school.reports.registers;

public class VaccinationRegisterData {
	
	private String condition = "";
	private String adverseEffects = "";
	private int under1Male;
	private int under1Female;
	private int onePlusMale;
	private int onePlusFemale;
	
	
	public String getCondition() {
		return condition;
	}
	public void setCondition(String condition) {
		this.condition = condition;
	}
	public String getAdverseEffects() {
		return adverseEffects;
	}
	public void setAdverseEffects(String adverseEffects) {
		this.adverseEffects = adverseEffects;
	}
	public int getUnder1Male() {
		return under1Male;
	}
	public void setUnder1Male(int under1Male) {
		this.under1Male = under1Male;
	}
	public int getUnder1Female() {
		return under1Female;
	}
	public void setUnder1Female(int under1Female) {
		this.under1Female = under1Female;
	}
	public int getOnePlusMale() {
		return onePlusMale;
	}
	public void setOnePlusMale(int onePlusMale) {
		this.onePlusMale = onePlusMale;
	}
	public int getOnePlusFemale() {
		return onePlusFemale;
	}
	public void setOnePlusFemale(int onePlusFemale) {
		this.onePlusFemale = onePlusFemale;
	}

}
